package com.example.enums;

/**
 * Enumeration representing different roles of users.
 */
public enum UserRole {
    
    /**
     * The Administrator role with full access.
     */
    ADMIN,

    /**
     * The Regular user role with standard access.
     */
    USER,

    /**
     * The Guest user role with limited access.
     */
    GUEST
}
